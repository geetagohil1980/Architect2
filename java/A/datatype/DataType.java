package A.datatype;



public class DataType {
    public static void main(String[] args) {

        // Integer Family
        // Byte range is -128 to 127

        byte a = 120;
        System.out.println(a);


        // Short range is -32768 to 32767
        short b =300;
        System.out.println(b);


        //int range is -2,147,483,648 to 2,147,483,647
        int c = -14567;
        System.out.println(c);


        // Long range is -9,223  to 9,223

        long d = 8128L;
        System.out.println(d);


        // Float family
        // Float range is 3.4E-38 to 3.4E+38
        float e = 132.777777777777777777777777777777777777777777777777777777777777F;
        System.out.println(e);

        // Double range is approximately 1.8 * 338
        double f = 1.6 * 234;
        System.out.println(f);

        // Character Family
        // Character range is unicode value ( singal range value )
        char  g = 'A';
        System.out.println(g);

        // Boolean Family
        // Boolean don't have any range,it's a fix value True or False
        boolean h =  true;
        System.out.println(h);

        // String Family
        // String range is 0 to 200
        String i = " My name is Geeta";
        System.out.println(i);

        // String Concatenation
           String  j = "Manual";
           String  k = "Automation";
           System.out.println( j+ " " +k  );







































    }
}
