package A.datatype;

public class Arithmetic {
    public static void main(String[] args) {

        int a = 5;
        int b = 10;
        System.out.println( a+b);
        System.out.println( a-b);
        System.out.println(b-a);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(b/a);
        System.out.println(++a);
        System.out.println( ++b);
        System.out.println(a--);
        System.out.println( --a);




    }
}
